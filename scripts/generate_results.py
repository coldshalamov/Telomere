#!/usr/bin/env python3
"""Generate docs/results.json and docs/RESULTS.md from real CLI runs."""

from __future__ import annotations

import hashlib
import json
import subprocess
import tempfile
from datetime import datetime, timezone
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
DOCS = ROOT / "docs"


def run(cmd: list[str]) -> subprocess.CompletedProcess[str]:
    return subprocess.run(
        cmd,
        cwd=ROOT,
        capture_output=True,
        text=True,
        check=True,
    )


def write_case_input(name: str, temp: Path) -> Path:
    path = temp / f"{name}.bin"
    if name == "deterministic-bytes":
        path.write_bytes(bytes(range(64)))
    elif name == "planted-sha256-arity2":
        block = hashlib.sha256(b"\x00").digest()[:4]
        path.write_bytes(block * 64)
    else:
        raise ValueError(name)
    return path


def case_config(name: str) -> dict[str, str]:
    if name == "deterministic-bytes":
        return {"block_size": "4", "hasher": "blake3"}
    if name == "planted-sha256-arity2":
        return {"block_size": "2", "hasher": "sha256"}
    raise ValueError(name)


def run_case(name: str, temp: Path) -> dict[str, object]:
    input_path = write_case_input(name, temp)
    output_path = temp / f"{name}.tlmr"
    restored_path = temp / f"{name}.restored"
    cfg = case_config(name)

    compress_cmd = [
        "cargo",
        "run",
        "--quiet",
        "--bin",
        "telomere",
        "--",
        "compress",
        str(input_path),
        str(output_path),
        "--block-size",
        cfg["block_size"],
        "--seed-depth",
        "1",
        "--passes",
        "1",
        "--hasher",
        cfg["hasher"],
        "--memory-limit",
        "100%",
        "--json",
        "--verify",
        "--force",
    ]
    proc = run(compress_cmd)
    summary = json.loads(proc.stdout)

    run(
        [
            "cargo",
            "run",
            "--quiet",
            "--bin",
            "telomere",
            "--",
            "decompress",
            str(output_path),
            str(restored_path),
            "--force",
        ]
    )
    if restored_path.read_bytes() != input_path.read_bytes():
        raise RuntimeError(f"{name}: decompressed bytes did not match input")

    input_bytes = input_path.stat().st_size
    output_bytes = output_path.stat().st_size
    return {
        "name": name,
        "input_bytes": input_bytes,
        "output_bytes": output_bytes,
        "delta_bytes": output_bytes - input_bytes,
        "delta_pct": ((output_bytes - input_bytes) / input_bytes * 100.0)
        if input_bytes
        else 0.0,
        "block_size": int(cfg["block_size"]),
        "seed_depth": 1,
        "hasher": cfg["hasher"],
        "summary": summary,
    }


def write_results(results: list[dict[str, object]]) -> None:
    DOCS.mkdir(exist_ok=True)
    payload = {
        "generated_by": "scripts/generate_results.py",
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "format_version": 1,
        "results": results,
    }
    (DOCS / "results.json").write_text(json.dumps(payload, indent=2) + "\n")

    lines = [
        "# Telomere Results",
        "",
        "Generated by `scripts/generate_results.py` from real CLI compress/decompress runs.",
        "Do not hand-edit benchmark tables; regenerate this file instead.",
        "",
        "| case | hasher | block size | seed depth | input bytes | output bytes | delta bytes | delta |",
        "| --- | --- | ---: | ---: | ---: | ---: | ---: | ---: |",
    ]
    for item in results:
        lines.append(
            "| {name} | {hasher} | {block_size} | {seed_depth} | {input_bytes} | "
            "{output_bytes} | {delta_bytes:+} | {delta_pct:+.2f}% |".format(**item)
        )
    lines.extend(
        [
            "",
            "Interpretation:",
            "",
            "- `deterministic-bytes` is ordinary non-planted data and is expected to bloat at seed depth 1.",
            "- `planted-sha256-arity2` is a deterministic corpus made from repeated `SHA256([0x00])[0..4]` spans; it proves the mechanism can produce negative delta when short seeds are deliberately present.",
            "- These are correctness and mechanism checks, not production performance claims.",
        ]
    )
    (DOCS / "RESULTS.md").write_text("\n".join(lines) + "\n")


def main() -> None:
    with tempfile.TemporaryDirectory() as tmp:
        temp = Path(tmp)
        results = [
            run_case("deterministic-bytes", temp),
            run_case("planted-sha256-arity2", temp),
        ]
    write_results(results)


if __name__ == "__main__":
    main()
